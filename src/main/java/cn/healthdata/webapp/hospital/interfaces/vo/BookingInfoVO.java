package cn.healthdata.webapp.hospital.interfaces.vo;

public class BookingInfoVO {
	private String booking_user_name;
	private String booking_user_age;
	private String booking_user_gender;
	private String booking_user_phone;
	private String booking_comment;
	
	public String getBooking_user_name() {
		return booking_user_name;
	}
	public void setBooking_user_name(String booking_user_name) {
		this.booking_user_name = booking_user_name;
	}
	public String getBooking_user_age() {
		return booking_user_age;
	}
	public void setBooking_user_age(String booking_user_age) {
		this.booking_user_age = booking_user_age;
	}
	public String getBooking_user_gender() {
		return booking_user_gender;
	}
	public void setBooking_user_gender(String booking_user_gender) {
		this.booking_user_gender = booking_user_gender;
	}
	public String getBooking_user_phone() {
		return booking_user_phone;
	}
	public void setBooking_user_phone(String booking_user_phone) {
		this.booking_user_phone = booking_user_phone;
	}
	public String getBooking_comment() {
		return booking_comment;
	}
	public void setBooking_comment(String booking_comment) {
		this.booking_comment = booking_comment;
	}
	
	
}
